package com.talkweb.xwzcxt.pojo;

public class Pie {
	
	
	private String name;
	private float y;
	
	
	public Pie(String name, float y) {
		super();
		this.name = name;
		this.y = y;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	
}
